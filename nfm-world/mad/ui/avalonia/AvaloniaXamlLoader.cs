namespace Avalonia.Markup.Xaml;

/// <summary>
/// Provides access to the root object during XAML instantiation.
/// </summary>
public interface IRootObjectProvider
{
    /// <summary>
    /// The root object of the xaml file
    /// </summary>
    object RootObject { get; }
    /// <summary>
    /// The "current" root object, contains either the root of the xaml file
    /// or the root object of the control/data template 
    /// </summary>
    object IntermediateRootObject { get; }
}

/// <summary>
/// Provides URI context during XAML loading.
/// </summary>
public interface IUriContext
{
    /// <summary>
    /// Gets or sets the base URI of the current XAML document.
    /// </summary>
    Uri? BaseUri { get; set; }
}

/// <summary>
/// Provides information about the target of a markup extension.
/// </summary>
public interface IProvideValueTarget
{
    /// <summary>
    /// Gets the target object where a value is being set.
    /// </summary>
    object? TargetObject { get; }

    /// <summary>
    /// Gets the target property where a value is being set.
    /// </summary>
    object? TargetProperty { get; }
}

public class AvaloniaXamlLoader
{
    /// <summary>
    /// Creates a service provider with default services for XAML instantiation.
    /// </summary>
    public static IServiceProvider CreateDefaultServiceProvider(object? rootObject = null, string? baseUri = null)
    {
        var provider = new XamlServiceProvider();

        var rootObjectProvider = new RootObjectProviderImpl
        {
            RootObject = rootObject,
            IntermediateRootObject = rootObject
        };
        provider.AddService<IRootObjectProvider>(rootObjectProvider);

        var uriContext = new UriContextImpl
        {
            BaseUri = baseUri != null ? new Uri(baseUri) : null
        };
        provider.AddService<IUriContext>(uriContext);

        var provideValueTarget = new ProvideValueTargetImpl();
        provider.AddService<IProvideValueTarget>(provideValueTarget);

        return provider;
    }

    /// <summary>
    /// Populates a XAML-based view by calling its generated Populate method.
    /// </summary>
    /// <typeparam name="T">The type of the view (must have a static Populate method generated by XamlX).</typeparam>
    /// <param name="instance">The instance to populate.</param>
    /// <param name="serviceProvider">Optional service provider. If null, a default one is created.</param>
    public static void Load<T>(T instance, IServiceProvider? serviceProvider = null) where T : class
    {
        serviceProvider ??= CreateDefaultServiceProvider(instance);

        var type = typeof(T);
        var populateMethod = type.GetMethod("Populate",
            System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Static);

        if (populateMethod == null)
        {
            throw new InvalidOperationException(
                $"Type {type.FullName} does not have a generated Populate method. " +
                "Ensure the XAML file is properly compiled with the XamlX build task.");
        }

        populateMethod.Invoke(null, [serviceProvider, instance]);
    }

    /// <summary>
    /// Creates and populates a XAML-based view.
    /// </summary>
    /// <typeparam name="T">The type of the view (must have a static Build method generated by XamlX).</typeparam>
    /// <param name="serviceProvider">Optional service provider. If null, a default one is created.</param>
    /// <returns>A new instance of the view with XAML properties applied.</returns>
    public static T Create<T>(IServiceProvider? serviceProvider = null) where T : class
    {
        var type = typeof(T);
        var buildMethod = type.GetMethod("Build",
            System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Static);

        if (buildMethod == null)
        {
            throw new InvalidOperationException(
                $"Type {type.FullName} does not have a generated Build method. " +
                "Ensure the XAML file is properly compiled with the XamlX build task.");
        }

        serviceProvider ??= CreateDefaultServiceProvider();

        return (T)buildMethod.Invoke(null, [serviceProvider])!;
    }
}